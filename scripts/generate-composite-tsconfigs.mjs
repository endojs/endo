#!/usr/bin/env node
/* eslint-disable @jessie.js/safe-await-separator */

/* eslint-disable no-continue */

/**
 * Generates per-package `tsconfig.composite.json` files and a root
 * `tsconfig.composite.json` that wires them all together via TypeScript project
 * references.  Run with `--check` to verify on-disk configs are up to date
 * (exits non-zero on drift; useful in CI).
 *
 * @module
 * @example
 * ```sh
 * node scripts/generate-composite-tsconfigs.mjs          # write files
 * node scripts/generate-composite-tsconfigs.mjs --check  # CI drift check
 * ```
 */

import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import { access, readFile, writeFile } from 'node:fs/promises';
import { relative, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const execFileAsync = promisify(execFile);

// ---------------------------------------------------------------------------
// Pure helpers (exported for tests)
// ---------------------------------------------------------------------------

/**
 * Parse the NDJSON output of `yarn workspaces list -R --json -v` into a
 * name → location map.  The root workspace entry (`name: null`) is skipped.
 *
 * @param {string} ndjson - Raw stdout from yarn.
 * @returns {Map<string, string>} Package name to relative workspace location.
 */
export const parseYarnWorkspaces = ndjson => {
  /** @type {Map<string, string>} */
  const map = new Map();
  for (const line of ndjson.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const entry = JSON.parse(trimmed);
    if (entry.name === null) continue; // skip root
    map.set(entry.name, entry.location);
  }
  return map;
};

/**
 * Extract the set of workspace package names that are *runtime* dependencies
 * of a given package: `dependencies`, `peerDependencies`, and
 * `optionalDependencies` whose version spec starts with `workspace:`.
 * `devDependencies` are intentionally excluded.
 *
 * @param {Record<string, any>} packageJson - Parsed package.json contents.
 * @returns {Set<string>} Workspace package names.
 */
export const getRuntimeWorkspaceDeps = packageJson => {
  const names = new Set();
  for (const field of [
    'dependencies',
    'peerDependencies',
    'optionalDependencies',
  ]) {
    const deps = packageJson[field];
    if (!deps) continue;
    for (const [name, version] of Object.entries(deps)) {
      if (String(version).startsWith('workspace:')) {
        names.add(name);
      }
    }
  }
  return names;
};

/**
 * Detect the first cycle in a directed graph using DFS.  Returns the cycle
 * path as an array of locations (e.g. `['packages/a', 'packages/b',
 * 'packages/a']`), or `null` if the graph is a DAG.
 *
 * @param {Map<string, string[]>} graph - location → list of dependency locations.
 * @returns {string[] | null}
 */
export const detectCycle = graph => {
  const visited = new Set();
  const stack = new Set();

  /**
   * @param {string} node
   * @param {string[]} path
   * @returns {string[] | null}
   */
  function dfs(node, path) {
    if (stack.has(node)) return [...path, node];
    if (visited.has(node)) return null;
    visited.add(node);
    stack.add(node);
    for (const dep of graph.get(node) ?? []) {
      const cycle = dfs(dep, [...path, node]);
      if (cycle) return cycle;
    }
    stack.delete(node);
    return null;
  }

  for (const node of graph.keys()) {
    const cycle = dfs(node, []);
    if (cycle) return cycle;
    if (cycle) return cycle;
  }
  return null;
};

/**
 * Generate the content of a per-package `tsconfig.composite.json`.
 *
 * @param {string[]} refPaths - Sorted relative paths to referenced tsconfig files.
 * @returns {object} Parsed config object (not yet serialised).
 */
export const makePackageCompositeConfig = refPaths => {
  return {
    extends: './tsconfig.build.json',
    compilerOptions: { composite: true },
    references: refPaths.map(p => ({ path: p })),
  };
};

/**
 * Generate the content of the root `tsconfig.composite.json`.
 *
 * @param {string[]} refPaths - Sorted relative paths from root to each participant.
 * @returns {object} Parsed config object.
 */
export const makeRootCompositeConfig = refPaths => {
  return {
    files: [],
    references: refPaths.map(p => ({ path: p })),
  };
};

const WARNING = `// DO NOT EDIT! THIS FILE IS AUTO-GENERATED BY scripts/generate-composite-tsconfigs.mjs\n`;

/**
 * Serialise a config object to a JSON string in the style used by this repo
 * (2-space indent, trailing newline).
 *
 * @param {object} config
 * @returns {string}
 */
export const serialise = config => {
  return `${WARNING}${JSON.stringify(config, null, 2)}\n`;
};

// ---------------------------------------------------------------------------
// Core logic
// ---------------------------------------------------------------------------

/**
 * Build the full set of per-package configs and the root config, returning
 * a map of absolute file path → serialised content.  Nothing is written to
 * disk here; that separation makes the `--check` mode and tests trivial.
 *
 * @param {{
 *   rootDir: string,
 *   nameToLocation: Map<string, string>,
 *   participantLocations: Set<string>,
 *   getPackageJson: (location: string) => Promise<Record<string, any>>,
 * }} opts
 * @returns {Promise<Map<string, string>>} Absolute file path → file content.
 */
export const buildConfigs = async ({
  rootDir,
  nameToLocation,
  participantLocations,
  getPackageJson,
}) => {
  // location → [dep locations] (runtime workspace deps, participants only)
  /** @type {Map<string, string[]>} */
  const graph = new Map();

  await Promise.all(
    [...participantLocations].map(async location => {
      const pkgJson = await getPackageJson(location);
      const runtimeNames = getRuntimeWorkspaceDeps(pkgJson);

      const depLocations = [];
      for (const name of runtimeNames) {
        const depLocation = nameToLocation.get(name);
        if (depLocation === undefined) continue; // unknown package, skip
        if (!participantLocations.has(depLocation)) continue; // non-participant, skip
        depLocations.push(depLocation);
      }

      graph.set(location, depLocations.sort());
    }),
  );

  const cycle = detectCycle(graph);
  if (cycle) {
    throw new Error(
      `Cycle detected in workspace dependency graph: ${cycle.join(' → ')}\n` +
        `Run without --check to regenerate after resolving the cycle.`,
    );
  }

  /** @type {Map<string, string>} absolute path → content */
  const files = new Map();

  // Per-package configs
  for (const [location, depLocations] of graph) {
    const pkgDir = resolve(rootDir, location);
    const refPaths = depLocations.map(dep => {
      const depDir = resolve(rootDir, dep);
      const relativePath = relative(pkgDir, depDir);
      return join(relativePath, 'tsconfig.composite.json');
    });
    const config = makePackageCompositeConfig(refPaths);
    files.set(join(pkgDir, 'tsconfig.composite.json'), serialise(config));
  }

  // Root config
  const sortedLocations = [...participantLocations].sort();
  const rootRefPaths = sortedLocations.map(loc =>
    join(loc, 'tsconfig.composite.json'),
  );
  const rootConfig = makeRootCompositeConfig(rootRefPaths);
  files.set(join(rootDir, 'tsconfig.composite.json'), serialise(rootConfig));

  return files;
};

// ---------------------------------------------------------------------------
// I/O helpers
// ---------------------------------------------------------------------------

/**
 * Invoke `yarn workspaces list -R --json -v` from the given directory and
 * return its stdout.
 *
 * @param {string} cwd
 * @returns {Promise<string>}
 */
const runYarnWorkspacesList = async cwd => {
  const result = await execFileAsync(
    'yarn',
    ['workspaces', 'list', '-R', '--json', '-v'],
    {
      cwd,
      encoding: 'utf8',
    },
  );
  return result.stdout;
};

const main = async () => {
  const rootDir = fileURLToPath(new URL('..', import.meta.url));
  const checkMode = process.argv.includes('--check');

  const ndjson = await runYarnWorkspacesList(rootDir);
  const nameToLocation = parseYarnWorkspaces(ndjson);

  // Participants: workspaces that have a tsconfig.build.json
  const participantLocationsArray = await Promise.all(
    nameToLocation.values().map(async location => {
      try {
        await access(join(rootDir, location, 'tsconfig.build.json'));
        return location;
      } catch {
        return null;
      }
    }),
  );

  const participantLocations = new Set(
    participantLocationsArray.filter(Boolean),
  );

  /** @param {string} location */
  const getPackageJson = async location => {
    const pkgPath = join(rootDir, location, 'package.json');
    return JSON.parse(await readFile(pkgPath, 'utf8'));
  };

  const files = await buildConfigs({
    rootDir,
    nameToLocation,
    participantLocations,
    getPackageJson,
  });

  if (checkMode) {
    let drifted = false;
    await Promise.all(
      [...files].map(async ([absPath, expected]) => {
        const rel = relative(rootDir, absPath);
        /** @type {string|null} */
        let actual;
        try {
          actual = await readFile(absPath, 'utf8');
        } catch {
          actual = null;
        }
        if (actual !== expected) {
          console.error(
            actual === null ? `Missing: ${rel}` : `Drift detected: ${rel}`,
          );
          drifted = true;
        }
      }),
    );
    if (drifted) {
      console.error(
        '\nRun `yarn build:types:gen` to regenerate composite tsconfig files.',
      );
      process.exitCode = 1;
    }
    console.log('All composite tsconfig files are up to date.');
  } else {
    await Promise.all(
      [...files].map(async ([absPath, content]) => {
        await writeFile(absPath, content);
        console.log(`Wrote ${relative(rootDir, absPath)}`);
      }),
    );
  }
};

// ---------------------------------------------------------------------------
// Entry point (only runs when invoked directly, not when imported by tests)
// ---------------------------------------------------------------------------

const isMain =
  process.argv[1] !== undefined &&
  resolve(process.argv[1]) === resolve(fileURLToPath(import.meta.url));

if (isMain) {
  // eslint-disable-next-line no-void
  void main();
}
