/**
 * Generate help-text-data.js from help.md.
 *
 * This pre-computes the help text map so that help-text.js does not
 * need to import `fs` at module load time.  The XS daemon bundle
 * cannot resolve `fs`, so the data must be static.
 *
 * Usage: node packages/daemon/scripts/generate-help-text-data.mjs
 */
import '@endo/init';
import fs from 'fs';
import url from 'url';
import path from 'path';
import { parseHelpdown } from '../src/helpdown.js';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const helpMdPath = path.resolve(__dirname, '../src/help.md');
const outPath = path.resolve(__dirname, '../src/help-text-data.js');

const helpMd = fs.readFileSync(helpMdPath, 'utf-8');
const entries = parseHelpdown(helpMd);

// Build the source for help-text-data.js.
// Each entry is [entityName, { methodName: docString, ... }].
const lines = [
  '// @ts-check',
  '// AUTO-GENERATED by scripts/generate-help-text-data.mjs — do not edit.',
  '',
  "import harden from '@endo/harden';",
  '',
  '/** @type {Array<[string, Record<string, string>]>} */',
  'export const helpTextEntries = harden([',
];

for (const [name, helpObj] of entries) {
  lines.push(`  [${JSON.stringify(name)}, {`);
  for (const [key, value] of Object.entries(helpObj)) {
    lines.push(`    ${JSON.stringify(key)}: ${JSON.stringify(value)},`);
  }
  lines.push('  }],');
}

lines.push(']);');
lines.push('');

fs.writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${outPath} (${entries.length} entities)`);
